package Terrain.TerrainLocation;

public class TerrainLocation {
    private int y;
    private int x;

    public TerrainLocation(int x, int y) {
        this.y = y;
        this.x = x;
    }

    public int getY() {
        return this.y;
    }

    public int getX() {
        return this.x;
    }
}
